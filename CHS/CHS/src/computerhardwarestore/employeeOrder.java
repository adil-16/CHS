    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        try {
            ProductTable.setVisible(true);
            jPanel3.setVisible(false);
            String name = "";

                Connection mycon = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe", "hr", "hr");
                Statement stmt = mycon.createStatement();
                ProductTable.setFont(new Font("Serif", Font.BOLD, 15));
                DefaultTableModel model = (DefaultTableModel) ProductTable.getModel();
                model.setRowCount(0);
                ResultSet result= stmt.executeQuery("select * from order_details");
                int count = 0;
                
                while (result.next()) {
                    if(customername.getText().equals(result.getString(1))){
                        model.addRow(new Object[]{result.getInt(1),result.getString(2),result.getString(3),result.getString(4),result.getString(5),result.getString(6),"Sell"});
                        count++;
                    }
                }
                
                ResultSet result2= stmt.executeQuery("select * from buy_order_details");
                while (result2.next()) {
                    if(customername.getText().equals(result2.getString(1))){
                        model.addRow(new Object[]{result2.getInt(1),result2.getString(2),result2.getString(3),result2.getString(4),result2.getString(5),result2.getString(6),"Buy"});
                        count++;
                    }
                }
                if(count==0){
                    JOptionPane.showMessageDialog(null,"No Record Found for "+name);
                }
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_SearchActionPerformed

