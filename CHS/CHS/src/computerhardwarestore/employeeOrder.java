    private void ShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowActionPerformed
        try {
            jPanel3.setVisible(false);
            Connection mycon = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:xe", "hr", "hr");
            Statement stmt = mycon.createStatement();
            ProductTable.setFont(new Font("Serif", Font.BOLD, 15));
            DefaultTableModel model = (DefaultTableModel) ProductTable.getModel();

            model.setRowCount(0);
            
            ResultSet result22 = stmt.executeQuery("select customer_id,customer_name,product.product_name,orders.total_price, payment.payment_type,orders.date_of_order from customer join buys on buys.customer_customer_id = customer.customer_id join product on buys.product_product_id = product.product_id join order_items on order_items.product_product_id = product.product_id join orders on orders.order_id = order_items.order_order_id join payment on payment.order_order_id = orders.order_id");
            while (result22.next()) {
                model.addRow(new Object[]{result22.getInt(1),result22.getString(2),result22.getString(3),result22.getString(4),result22.getString(5),result22.getString(6),"Buy"});
            }
            
            ResultSet sellresult = stmt.executeQuery("select customer_id,customer_name,product.product_name,orders.total_price, payment.payment_type,orders.date_of_order from customer join sells on sells.customer_customer_id = customer.customer_id join product on sells.product_product_id = product.product_id join order_items on order_items.product_product_id = product.product_id join orders on orders.order_id = order_items.order_order_id join payment on payment.order_order_id = orders.order_id");
            while (sellresult.next()) {
                model.addRow(new Object[]{sellresult.getInt(1),sellresult.getString(2),sellresult.getString(3),sellresult.getString(4),sellresult.getString(5),sellresult.getString(6),"Sell"});
            }

        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_ShowActionPerformed

